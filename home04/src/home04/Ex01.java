package home04;

public class Ex01 {
	public static void main(String[] args) {
//		1부터 10까지의 숫자 중에서 홀수만 출력하기
		
		//정수형 변수 지정해주기
		//1~10까지 1씩 증가하는 반복문 출력(while)
		//그 중 홀수만 골라낼수있는 조건문(if) 써주기
		
		int num = 1;
		
		while(num <= 10) {
			if(num % 2 == 1) {
				System.out.println(num);				
			}
			++num;
		}
	}
}
